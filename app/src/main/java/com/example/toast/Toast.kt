package com.example.toast

/*Toast in Android
*
* What is Toast - This is a short lived message that appears on the screen to provide feedback or information to the user
*
* ->Toast are typically used to display non-intrusive notifications
*
*
*
* Key Components in a Toast
* ->Text message: The primary content of a toast is a short text message that conveys a message to the user.
* ->Duration: Toasts have a predefined duration for how long they are  displayed on the screen before they disappear automatically.
* ->Position: Toasts can be displayed at different positions on the screen, i.e Top,Center,Bottom. But by default they appear at the bottom of the screen*/